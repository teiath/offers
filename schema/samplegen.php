function openFile($name){
	$incFile = SAMPLE_PREFIX.$name.SAMPLE_EXTENTION;
	if($fh = fopen($incFile, 'w'))
		return $fh;
	else
		return false;
}

function writeFile($f, $data){
	fwrite($f, $data);
}

function closeFile($f){
	fclose($f);
